    <h2>Kegiatan</h2>
    <p>SMP Negeri 1 Sidoarjo menawarkan beragam kegiatan bagi para siswa, baik dalam bidang akademik maupun non-akademik. Beberapa kegiatan di SMP Negeri 1 Sidoarjo meliputi: <br>

Ekstrakurikuler Akademik <br><br>

1. Olimpiade Sains: Pelatihan dan kompetisi di bidang sains, seperti Matematika, Fisika, dan Biologi. <br>
2. English Club: Pengembangan kemampuan bahasa Inggris melalui debat, drama, dan public speaking. <br>
3. KIR (Kelompok Ilmiah Remaja): Mengasah keterampilan penelitian dan inovasi ilmiah. <br><br>
Ekstrakurikuler Seni dan Budaya <br>

1. Karawitan: Mengenalkan siswa pada seni musik tradisional Jawa. <br>
2. Tari Tradisional: Melatih tarian tradisional sebagai upaya melestarikan budaya lokal. <br>
3. Seni Lukis dan Seni Suara: Mendorong kreativitas dalam seni visual dan musik. <br><br>
Kegiatan Olahraga <br>

1. Sepak Bola: Tim sepak bola sekolah sering mengikuti turnamen tingkat kabupaten. <br>
2. Basket: Ekstrakurikuler yang cukup populer, dengan fasilitas lapangan yang mendukung. <br>
3. Pencak Silat: Membangun karakter melalui seni bela diri tradisional. <br><br>
Pramuka dan Paskibra <br>

1. Pramuka: Mengajarkan kemandirian, keterampilan, dan kerja sama. <br>
2. Paskibra: Tim pengibar bendera sekolah yang aktif berpartisipasi dalam kegiatan upacara. <br><br>
Pengembangan Diri dan Kepemimpinan <br>

OSIS (Organisasi Siswa Intra Sekolah): Wadah bagi siswa untuk berlatih kepemimpinan dan mengelola berbagai kegiatan sekolah. <br>
PMR (Palang Merah Remaja): Melatih siswa dalam pertolongan pertama dan kegiatan sosial. <br><br>
Kegiatan Religi <br>

1. Pengajian dan Rohis: Mendukung pembinaan keagamaan melalui kajian rutin dan kegiatan keagamaan lainnya. <br>
2. Peringatan Hari Besar Agama: Memperingati hari besar seperti Maulid Nabi, Idul Adha, dan kegiatan keagamaan lainnya. <br><br>
Berbagai kegiatan ini bertujuan untuk membentuk karakter siswa yang tidak hanya cerdas secara akademik tetapi juga berprestasi dalam keterampilan, memiliki jiwa sosial, serta mencintai budaya dan bangsa.</p>    
